import java.util.Scanner;

public class forcaseT{
    public static void main (String []args){
        Scanner sc = new Scanner (System.in);
        int t = sc.nextInt();
        for(int a= 1 ; a<=t ; a++ ){
            int b = sc.nextInt();
            int c = sc.nextInt();
            System.out.println("Case #" + a + ": " + (b + c));
            
            
        }
    }
}